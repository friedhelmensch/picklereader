var Contract = require("web3-eth-contract");
Contract.setProvider("https://arb1.arbitrum.io/rpc");

const getTotalSupply = async () => {
  const spellWethSlp = new Contract(
    slpAbi,
    "0x8f93eaae544e8f5eb077a1e09c1554067d9e2ca8"
  );

  const totalSupply = await spellWethSlp.methods.totalSupply().call();
  return totalSupply;
};

const getLpTokenAmount = async (shares) => {
  const pickleJar = new Contract(
    jarAbi,
    "0x9cae10143d7316df417413c43b79fb5b44fa85e2"
  );
  const balance = await pickleJar.methods.balance().call();
  const totalSupply = await pickleJar.methods.totalSupply().call();

  const lpTokens = (balance * shares) / totalSupply;
  return lpTokens;
};

const getBalanceOfSlp = async (abi, address) => {
  const weth = new Contract(abi, address);

  const balance = await weth.methods
    .balanceOf("0x8f93eaae544e8f5eb077a1e09c1554067d9e2ca8")
    .call();

  return balance;
};

const getPercentage = async () => {
  const totalSupply = await getTotalSupply();
  //todo get shares from pickl farm by address
  const shares = 386622347008259460887;
  const amount = await getLpTokenAmount(shares);
  console.log(`lpTokens: ${Math.round((amount / 1e18) * 100) / 100}`);

  const percentage = amount / totalSupply;
  return percentage;
};

const getWeth = async (percentage) => {
  const wethBalanceOfSlp = await getBalanceOfSlp(
    wethAbi,
    "0x82af49447d8a07e3bd95bd0d56f35241523fbab1"
  );
  const weth = wethBalanceOfSlp * percentage;
  return weth;
};

const getSpell = async (percentage) => {
  const spellBalanceOfSlp = await getBalanceOfSlp(
    spellAbi,
    "0x3e6648c5a70a150a88bce65f4ad4d506fe15d2af"
  );
  const spell = spellBalanceOfSlp * percentage;
  return spell;
};

const doAll = async () => {
  const percentage = await getPercentage();
  const weth = await getWeth(percentage);
  const spell = await getSpell(percentage);

  const spellPriceResponse = await axios.get(
    "https://api.coingecko.com/api/v3/simple/price?ids=spell-token&vs_currencies=usd"
  );

  const ethPriceResponse = await axios.get(
    "https://api.coingecko.com/api/v3/simple/price?ids=ethereum&vs_currencies=usd"
  );

  const spellPrice = spellPriceResponse.data["spell-token"].usd;
  const ethPrice = ethPriceResponse.data["ethereum"].usd;

  console.log(`eth: ${weth / 1e18}`);
  console.log(`spell: ${spell / 1e18}`);
  const spellCosts = (spell / 1e18) * spellPrice;
  console.log(`spell in USD: ${spellCosts}`);
  const ethCosts = (weth / 1e18) * ethPrice;
  console.log(`eth in USD: ${ethCosts}`);
  console.log(`Total USD: ${spellCosts + ethCosts}`);
};

